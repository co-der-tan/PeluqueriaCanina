    private void cargarDatos(int num_cliente) {
        
        this.mascota = control.traerMascota(num_cliente);
        
        txtNombre.setText(mascota.getNombre());
        txtRaza.setText(mascota.getRaza());
        txtColor.setText(mascota.getColor());
        txtNombreDuenio.setText(mascota.getUnDuenio().getNombre());
        txtCelDueño.setText(mascota.getUnDuenio().getCelDuenio());
        jtaObservaciones.setText(mascota.getObservaciones());
        cmbAlergico.setSelectedItem(mascota.getAlergico());
        cmbAtencionEspecial.setSelectedItem(mascota.getAtencion_especial());
        
    }
