    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
       // datos de la mascota
        String nombreMasco = txtNombre.getText();
        String raza = txtRaza.getText();
        String color = txtColor.getText();
        String observaciones = jtaObservaciones.getText();
        String alergico = (String) cmbAlergico.getSelectedItem();
        String atEspecial = (String) cmbAtencionEspecial.getSelectedItem();
        
        // datos del dueño
        String nombreDuenio = txtNombreDuenio.getText();
        String celDuenio = txtCelDueño.getText();
        
        control.modificarMascota(mascota, nombreMasco, raza, color, observaciones, alergico,
                atEspecial, nombreDuenio, celDuenio );
        
       
        
        JOptionPane.showMessageDialog(this, "Guardado exitosamente!!!");
        
        this.dispose();
        
       VerDatos cargarTabla = new VerDatos();
       cargarTabla.cargarTabla();
       cargarTabla.setVisible(true);
       cargarTabla.setLocationRelativeTo(null);
    }//GEN-LAST:event_btnGuardarActionPerformed

    
   

